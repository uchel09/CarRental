class Car {
  final String name;
  final String brand;
  final double price;
  final String image;
  final double rating;
  final String description;
  final String speed;
  final String fuel;

  Car({
    required this.name,
    required this.brand,
    required this.price,
    required this.image,
    required this.rating,
    required this.description,
    required this.speed,
    required this.fuel,
  });
}
